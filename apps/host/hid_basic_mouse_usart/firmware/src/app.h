/* Application states

  Summary:
    Application states enumeration

  Description:
    This enumeration defines the valid application states.  These states
    determine the behavior of the application at various times.
*/

typedef enum
{
	/* Application's state machine's initial state. */
	APP_STATE_INIT=0,
	APP_STATE_SERVICE_TASKS,
    APP_STATE_WAIT_FOR_HOST_ENABLE,
    APP_STATE_WAIT_FOR_DEVICE_ATTACH,
    APP_STATE_DEVICE_ATTACHED,
    APP_STATE_READ_HID,
    APP_STATE_DEVICE_DETACHED,
    APP_USART_STATE_DRIVER_OPEN,
    APP_USART_STATE_CHECK_FOR_STRING_TO_SEND,
    APP_USART_STATE_DRIVER_WRITE,
    APP_STATE_ERROR

} APP_STATES;


// *****************************************************************************
/* Application Data

  Summary:
    Holds application data

  Description:
    This structure holds the application's data.

  Remarks:
    Application strings and buffers are be defined outside this structure.
 */
typedef uintptr_t DRV_USART_BUFFER_HANDLE;

typedef struct
{
    /* The application's current state */
    APP_STATES state;

    /* Mouse driver instance handle with which this application is
     * communicating */
    USB_HOST_HID_MOUSE_HANDLE handle;

    /* Application data buffer */
    USB_HOST_HID_MOUSE_DATA data;
    
    /* USART Application task state */
    APP_STATES usartTaskState;
    
    /* Unique handle to USART driver */
    DRV_HANDLE usartDriverHandle;
    
    /* Number of bytes written by the USART write*/
    uint32_t nBytesWritten;
    
    /* Size of the buffer to be written */
    uint32_t stringSize;
    
    /* Buffer used for USART writing */
    uint8_t string[64];
    
    /* Flag used to determine if data is to be written to USART */
    bool stringReady;
    
    /* Flag used to determine if device has been attached */
    bool isMouseAttached;
    
    DRV_USART_BUFFER_HANDLE WriteBufHandler;

} APP_DATA;


